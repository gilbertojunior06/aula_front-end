# operadores matemáticos (aritméticos)

# -------------------------------
# Parte 1 - Números inteiros
# -------------------------------
valor_num1 = int(input('Informe o valor 1: '))
valor_num2 = int(input('Informe o valor 2: '))

soma = valor_num1 + valor_num2
subtracao = valor_num1 - valor_num2
divisao = valor_num1 / valor_num2
multiplicacao = valor_num1 * valor_num2 
divisao_inteira = valor_num1 // valor_num2
modulo = valor_num1 % valor_num2
exponenciacao = valor_num1 ** valor_num2

print(f'Soma: {soma}')
print(f'Subtração: {subtracao}')
print(f'Divisão: {divisao}')
print(f'Multiplicação: {multiplicacao}')
print(f'Divisão inteira: {divisao_inteira}')
print(f'Módulo: {modulo}')
print(f'Exponenciação: {exponenciacao}')

